<h1 id="helpt">Käyttöohjeet</h1>

<p id="help">"Etsi lähin" -nappia painamalla sovellus hakee omaa sijaintiasi lähinnä olevan opiskelijaravintolan. 
<br/><br/>
Käyttämäsi laitteen tulee tunnistaa oma sijaintinsa. Puhelimissa ja tablettitietokoneissa on usein sisäinen paikannusjärjestelmä, jonka toiminta sinun täytyy sallia. 
Jos käytät sovellusta tietokoneella, on varminta kun syötät sijaintisi itse. Oman sijainnin voit antaa sovellukselle kirjoittamalla "Oma sijainti" kenttään oman osoitteesi.
<br/><br/>
"Seuraava" nappia painamalla voit hakea reitin seuraavaksi lähimpään ravintolaan. Sovellus näyttää sinulle vuorotellen reitin kaikkiin opiskelijaravintoloihin 
2 km säteellä omasta sijainnistasi. "Näytä sijainti" -napilla voit aina palata takaisin omaan sijaintiisi.
<br/><br/>
Ehdotetulle ravintolalle avautuu musta puhekupla, josta selviää ravintolan nimi, osoite, etäisyys ravintolaan ja linkki ruokalistaan. 
Infolaatikon voit sulkea oikeassa yläkulmassa olevasta ruksista. Laatikon saa auki myös ravintolan ikonia klikkaamalla.
<br/><br/>
Jos huomaat ravintoloiden tiedoissa virheitä tai ravintolan, jota ei enää ole, voit klikata mustan infolaatikon oikeassa alakulmassa olevaa kynäsymbolia ja 
ehdottaa sovelluksen kehittäjille tietojen päivittämistä tai ravintolan poistoa. Jos haluat ehdottaa uuden ravintolan tietojen lisäämistä sovellukseen, 
paina koko karttaruudun oikeassa alakulmassa punaisella pohjalla olevaa mustaa + merkkiä.
</p>
